public class JavaOperators{
	 public static void main(String[] args){
		 System.out.println("Java Exercise [Operators] Exercise [1]");
		 System.out.println(10*5 + "\n");
		 System.out.println("Java Exercise [Operators] Exercise [2]");
		 System.out.println(10/5 + "\n");
		 System.out.println("Java Exercise [Operators] Exercise [3]");
		 int x = 10;
		 ++x;
		 System.out.println(x + "\n");
		 System.out.println("Java Exercise [Operators] Exercise [4]");
		 x = 10;
		 x += 5;
		 System.out.println(x + "\n");
	 }
}